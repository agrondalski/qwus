<b>2. SIGN UPS</b>
<br>

<br>
This NQR NA tournament will be open format. Any team from anywhere may enter the tournament. There are no limitations on the makeup of teams prior to sign up. Post sign up all teams will be given two slots which they can choose to fill or not before the end of (let's say) week 4. After week 4 any open slots are forfeited.
