<br>
<b>7. CHOOSING MAPS</b>
<br>

<br>
Each regular season match is best of 3. Each team pick one map each. Third map is chosen by each team tossing 2 maps (alternate tossing a map until 1 is left) from the pool of 7 the remaining 5 maps. The map that is not tossed is played.
<br>

<br>
<b>8. SERVERS</b>
<br>

<br>
Servers should be chosen based on their fairness in average ping times. A 90ms vs 90ms average ping server would be chosen over 70ms vs 50ms, etc. If you are an east coast team playing a west coast team expect to play central. As long as ping averages are within 13ms there is no reason to switch servers unless something better can be found.
<br>

<br>
Minping can be used in matches of NA teams vs. NA teams or International Teams vs International Teams, however, all matches must be played in North America. International teams are subject to the whims of playing in North America and the unfairness of pings that comes with such a choice.
<br>

